    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        // TODO add your handling code here:
        
        double A, B, C;
                
        A = Double.parseDouble(txtA.getText());
        B = Double.parseDouble(txtB.getText());
        C = Double.parseDouble(txtC.getText());
        
        double raiz = Math.pow(B, 2) - 4 * A * C;
        double Solucion = 0;
        
        if (raiz < 0) {
            txtResultado.setText("No hay soluciones reales"); // porque la raiz no puede ser cero
        } else if (raiz == 0) {
            Solucion = -B / (2 * A);
            txtResultado.setText("La soluciÃ³n es: " + Solucion);
        } else {
            double Solucion1 = (-B + Math.sqrt(raiz)) / (2 * A);
            double Solucion2 = (-B - Math.sqrt(raiz)) / (2 * A);
            txtResultado.setText("Las posibles soluciones son: " + Solucion1 + " y " + Solucion2);
        }
        
        
        
        
    }//GEN-LAST:event_btnCalcularActionPerformed

